#include "libft.h"

void	ft_putstr(char *s)
{
	int i;

	i = 0;
	if (!s)
	{
		free(s);
		return ;
	}
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}
